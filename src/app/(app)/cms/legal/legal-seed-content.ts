export type CompanyProfileLike = {
  name?: string
  afm?: string
  doy?: string
  address?: string
  city?: string
  zip?: string
  country?: string
  email?: string
  phone?: string
  website?: string
}

export type LegalSeedPage = { slug: string; title: string; body: string }

function placeholder(label: string): string {
  return `[ΣΥΜΠΛΗΡΩΣΕ: ${label}]`
}

function controllerBlock(profile: CompanyProfileLike | null): string {
  const name = profile?.name?.trim() || placeholder('επωνυμία εταιρείας')
  const afm = profile?.afm?.trim() || placeholder('ΑΦΜ')
  const doy = profile?.doy?.trim() || placeholder('ΔΟΥ')
  const addressParts = [profile?.address?.trim(), profile?.city?.trim(), profile?.zip?.trim(), profile?.country?.trim()].filter(
    (part): part is string => Boolean(part),
  )
  const address = addressParts.length > 0 ? addressParts.join(', ') : placeholder('διεύθυνση έδρας')
  const email = profile?.email?.trim() || placeholder('email επικοινωνίας για θέματα προσωπικών δεδομένων')
  const phone = profile?.phone?.trim() || placeholder('τηλέφωνο επικοινωνίας')
  const website = profile?.website?.trim() || placeholder('ιστότοπος')

  return [
    `- **Επωνυμία:** ${name}`,
    `- **ΑΦΜ / ΔΟΥ:** ${afm} / ${doy}`,
    `- **Έδρα:** ${address}`,
    `- **Email:** ${email}`,
    `- **Τηλέφωνο:** ${phone}`,
    `- **Ιστότοπος:** ${website}`,
  ].join('\n')
}

